# 在线 API 获取
# ════════════════════════════════════════════════════════

HITOKOTO_URL = "https://v1.hitokoto.cn/?c=d"  # 励志分类
USER_AGENT = "Mozilla/5.0 (Windows NT 10.0; Win64; x64) AppleWebKit/537.36"


def _fetch_online() -> Optional[str]:
    """从一言 API 获取今日微语"""
    req = urllib.request.Request(HITOKOTO_URL, headers={"User-Agent": USER_AGENT})
    ctx = ssl.create_default_context()

    try:
        with urllib.request.urlopen(req, context=ctx, timeout=8) as resp:
            data = json.loads(resp.read().decode("utf-8"))
        text = data.get("hitokoto", "").strip()
        source = data.get("from", "").strip()
        if text:
            if source:
                return f"{text} —— {source}"
            return text
    except (urllib.error.URLError, urllib.error.HTTPError, OSError, json.JSONDecodeError):
        pass

    return None


def _get_local(date_dt: datetime) -> str:
    """从本地金句库按日期取一条"""
    # 用日期做种子，保证同一天取到同一条
    day_of_year = date_dt.timetuple().tm_yday
    index = (day_of_year - 1) % len(MOTTO_POOL)
    return MOTTO_POOL[index]


def get_motto(date_dt: Optional[datetime] = None) -> str:
    """
    获取当天微语

    Args:
        date_dt: 日期（默认北京时间今天）

    Returns:
        微语字符串
    """
    if date_dt is None:
        date_dt = datetime.now(timezone(timedelta(hours=8)))

    # 优先在线获取
    online = _fetch_online()
    if online:
        return online

    # 失败则用本地库
    return _get_local(date_dt)
